def test_basic_graph_with_missing_hanging_node():
    """ Verifies if production 3 was not applied to a basic graph with missing hanging node"""
    G = prepare_valid_test_graph_p3()
    G.remove_node('v:1.0:0.5')
    G.add_edges_from([
        ('v:1.0:1.0', 'v:1.0:0.0', {'label': 'E', 'B': 1}),
    ])
    expected_graph = nx.to_dict_of_dicts(G)

    prod3 = ProductionP3(G)
    prod3.apply()

    assert expected_graph == nx.to_dict_of_dicts(G)


